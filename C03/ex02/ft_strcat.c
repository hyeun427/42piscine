char	*ft_strcat(char *dest, char *src)
{
	char *str;

	str = dest;
	while (*str != '\0')
		str++;
	while (*src != '\0')
	{
		*str = *src;
		++src;
		++str;
	}
	*str = '\0';
	return (dest);
}
