#include <unistd.h>

int		main(void)
{
	char *alpha;
	int i;
	
	*alpha = 'a';
	i = 0;
	while (*alpha <= 'z' || i != '\0')
	{	
		if(i % 2 == 1)
			alpha[i] = alpha[i] - 32;
		write(1, &alpha, 1);
		i++;
		alpha++;
	}
	*alpha = '\n';
	return (0);
}
